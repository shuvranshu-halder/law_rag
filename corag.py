def format_intermediate_qa(steps: list) -> str:
    if not steps:
        return "None"
    lines = []
    for i, s in enumerate(steps, 1):
        lines.append(f"Q{i}: {s['sub_query']}")
        lines.append(f"A{i}: {s['sub_answer']}")
    return "\n".join(lines)


def format_docs(docs: List[str]) -> str:
    return "\n\n".join([f"Document {i+1}: {d}" for i, d in enumerate(docs)])


