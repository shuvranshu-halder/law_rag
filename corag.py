def corag_best_of_n(
    query: str, vectorstore, model, tokenizer,
    gen_greedy, gen_sample,
    N: int = None, max_length: int = None
) -> RetrievalChain:
    """
    Best-of-N Sampling: Sample N chains with temperature, pick lowest penalty.
    """
    N = N or Config.NUM_SAMPLES
    L = max_length or Config.MAX_CHAIN_LENGTH

    chains = []
    for _ in range(N):
        chain = RetrievalChain(query=query)
        for _ in range(L):
            sq_prompt = fmt_sub_query_prompt(query, chain.get_intermediate_qa())
            sub_query = generate_text(model, tokenizer, sq_prompt, gen_sample)
            if not sub_query:
                break
            existing = {s.sub_query.lower() for s in chain.steps}
            if sub_query.lower() in existing:
                break

            docs = retrieve_docs(vectorstore, sub_query)
            sa_prompt = fmt_sub_answer_prompt(sub_query, format_docs(docs))
            sub_answer = generate_text(model, tokenizer, sa_prompt, gen_greedy)
            chain.steps.append(RetrievalStep(sub_query, sub_answer, docs))

        chain.penalty_score = compute_penalty(chain)
        chains.append(chain)

    # Select best chain (lowest penalty)
    best = min(chains, key=lambda c: c.penalty_score)

    # Final answer only for the best chain
    orig_docs = retrieve_docs(vectorstore, query)
    all_docs = format_docs(orig_docs[:5]) + "\n\n" + best.get_all_docs()
    fa_prompt = fmt_final_answer_prompt(query, best.get_intermediate_qa(), all_docs)
    best.final_answer = generate_text(model, tokenizer, fa_prompt, gen_greedy)
    return best


