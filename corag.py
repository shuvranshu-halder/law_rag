@dataclass
class RetrievalStep:
    sub_query: str
    sub_answer: str
    retrieved_docs: List[str] = field(default_factory=list)

@dataclass
class RetrievalChain:
    query: str
    steps: List[RetrievalStep] = field(default_factory=list)
    final_answer: str = ""
    penalty_score: float = float('inf')

    def get_intermediate_qa(self) -> str:
        return format_intermediate_qa(
            [{"sub_query": s.sub_query, "sub_answer": s.sub_answer} for s in self.steps]
        )

    def get_all_docs(self) -> str:
        seen = set()
        docs = []
        for step in self.steps:
            for doc in step.retrieved_docs:
                if doc not in seen:
                    seen.add(doc)
                    docs.append(doc)
        return format_docs(docs[:15])


