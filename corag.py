def build_vectorstore(dataset_split) -> FAISS:
    """Build FAISS index from SQuAD v2 validation contexts."""
    logger.info("Building FAISS vector store from evaluation contexts...")

    all_text = ""
    seen = set()
    for row in dataset_split:
        ctx = row["context"].strip()
        if ctx not in seen:
            seen.add(ctx)
            all_text += ctx + "\n"

    splitter = RecursiveCharacterTextSplitter(
        chunk_size=Config.CHUNK_SIZE,
        chunk_overlap=Config.CHUNK_OVERLAP,
        separators=["\n\n", "\n", " ", ""]
    )
    chunks = splitter.split_text(all_text)
    logger.info(f"Total chunks: {len(chunks)}")

    embeddings = HuggingFaceEmbeddings(
        model_name=Config.EMBEDDING_MODEL,
        model_kwargs={"device": "cuda:0"},
        encode_kwargs={"normalize_embeddings": True}
    )

    vectorstore = FAISS.from_texts(chunks, embeddings)
    logger.info(f"FAISS index built with {vectorstore.index.ntotal} vectors")
    return vectorstore


def load_vectorstore() -> FAISS:
    """Load existing FAISS index."""
    embeddings = HuggingFaceEmbeddings(
        model_name=Config.EMBEDDING_MODEL,
        model_kwargs={"device": "cuda:0"},
        encode_kwargs={"normalize_embeddings": True}
    )
    return FAISS.load_local(
        Config.FAISS_PATH, embeddings,
        allow_dangerous_deserialization=True
    )


def retrieve_docs(vectorstore, query: str, top_k: int = None) -> List[str]:
    """Retrieve top-k documents using LangChain FAISS retriever."""
    k = top_k or Config.RETRIEVER_TOP_K
    retriever = vectorstore.as_retriever(
        search_type="similarity",
        search_kwargs={"k": k}
    )
    docs = retriever.invoke(query)
    return [d.page_content for d in docs]


