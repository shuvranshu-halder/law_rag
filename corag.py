def normalize_answer(s: str) -> str:
    """Normalize answer for evaluation (from SQuAD eval script)."""
    s = s.strip().lower()
    s = re.sub(r'\b(a|an|the)\b', ' ', s)
    # Remove punctuation
    s = ''.join(ch for ch in s if ch not in set(string.punctuation))
    # Fix whitespace
    s = ' '.join(s.split())
    if s in ["no answer", "no answers", "n/a", "no relevant information found", ""]:
        return ""
    return s


def compute_em(pred: str, gt: str) -> int:
    """Exact Match score (Section 4.1)."""
    return int(normalize_answer(pred) == normalize_answer(gt))


def compute_f1(pred: str, gt: str) -> float:
    """Token-level F1 score (Section 4.1)."""
    p_tok = normalize_answer(pred).split()
    g_tok = normalize_answer(gt).split()
    if not g_tok:
        return 1.0 if not p_tok else 0.0
    if not p_tok:
        return 0.0
    common = Counter(p_tok) & Counter(g_tok)
    nc = sum(common.values())
    if nc == 0:
        return 0.0
    prec = nc / len(p_tok)
    rec = nc / len(g_tok)
    return 2 * prec * rec / (prec + rec)


def compute_substring(pred: str, gt: str) -> int:
    """Substring match (your original metric)."""
    p, g = normalize_answer(pred), normalize_answer(gt)
    if not g:
        return 1 if not p else 0
    return int(g in p)


