def corag_tree_search(
    query: str, vectorstore, model, tokenizer,
    gen_greedy, gen_sample, max_length: int = None
) -> RetrievalChain:
    """
    Tree Search (BFS with rollouts):
    At each step, expand by sampling several sub-queries.
    For each, perform rollouts to estimate quality. Keep the best.
    """
    L = max_length or Config.MAX_CHAIN_LENGTH
    exp_size = Config.TREE_EXPANSION
    n_rollouts = Config.TREE_ROLLOUTS
    rollout_depth = Config.TREE_ROLLOUT_DEPTH

    current = RetrievalChain(query=query)

    for depth in range(L):
        candidates = []

        for _ in range(exp_size):
            sq_prompt = fmt_sub_query_prompt(query, current.get_intermediate_qa())
            sub_query = generate_text(model, tokenizer, sq_prompt, gen_sample)
            if not sub_query:
                continue
            existing = {s.sub_query.lower() for s in current.steps}
            if sub_query.lower() in existing:
                continue

            docs = retrieve_docs(vectorstore, sub_query)
            sa_prompt = fmt_sub_answer_prompt(sub_query, format_docs(docs))
            sub_answer = generate_text(model, tokenizer, sa_prompt, gen_greedy)

            expanded = RetrievalChain(
                query=query,
                steps=deepcopy(current.steps) + [RetrievalStep(sub_query, sub_answer, docs)]
            )

            # Rollouts to estimate quality
            rollout_penalties = []
            for _ in range(n_rollouts):
                rollout = RetrievalChain(query=query, steps=deepcopy(expanded.steps))
                for _ in range(rollout_depth):
                    rq_prompt = fmt_sub_query_prompt(query, rollout.get_intermediate_qa())
                    rq = generate_text(model, tokenizer, rq_prompt, gen_sample)
                    if not rq:
                        break
                    r_existing = {s.sub_query.lower() for s in rollout.steps}
                    if rq.lower() in r_existing:
                        break
                    rd = retrieve_docs(vectorstore, rq)
                    ra_prompt = fmt_sub_answer_prompt(rq, format_docs(rd))
                    ra = generate_text(model, tokenizer, ra_prompt, gen_greedy)
                    rollout.steps.append(RetrievalStep(rq, ra, rd))
                rollout_penalties.append(compute_penalty(rollout))

            avg_pen = np.mean(rollout_penalties) if rollout_penalties else float('inf')
            candidates.append((expanded, avg_pen))

        if not candidates:
            break

        best_exp, _ = min(candidates, key=lambda x: x[1])
        current = best_exp

    # Final answer
    orig_docs = retrieve_docs(vectorstore, query)
    all_docs = format_docs(orig_docs[:5]) + "\n\n" + current.get_all_docs()
    fa_prompt = fmt_final_answer_prompt(query, current.get_intermediate_qa(), all_docs)
    current.final_answer = generate_text(model, tokenizer, fa_prompt, gen_greedy)
    current.penalty_score = compute_penalty(current)
    return current


