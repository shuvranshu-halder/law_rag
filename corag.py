def compute_penalty(chain: RetrievalChain) -> float:
    """
    From Section 3.3: Penalty = fraction of steps returning no useful info.
    Lower penalty = better chain.
    """
    if not chain.steps:
        return float('inf')
    no_info = 0
    for step in chain.steps:
        ans = step.sub_answer.lower()
        if ("no relevant information" in ans or "not found" in ans or
            "no information" in ans or ans.strip() == ""):
            no_info += 1
    return no_info / len(chain.steps)


