def generate_text(model, tokenizer, prompt, gen_config):
    try:
        inputs = tokenizer(prompt, return_tensors="pt", truncation=True, max_length=2048)
        inputs = {k: v.to(model.device) for k, v in inputs.items()}

        with torch.no_grad():
            output_ids = model.generate(
                **inputs,
                generation_config=gen_config,
                pad_token_id=tokenizer.eos_token_id,
            )

        new_tokens = output_ids[0, inputs["input_ids"].shape[1]:]
        text = tokenizer.decode(new_tokens, skip_special_tokens=True).strip()

        # Clean: take only first sentence/line, remove repeated fragments
        lines = [l.strip() for l in text.split("\n") if l.strip()]
        for line in lines:
            if not line.startswith("#") and len(line) > 2:
                text = line
                break
        else:
            text = lines[0] if lines else ""

        # Cut off at first repetition pattern
        words = text.split()
        clean_words = []
        for w in words:
            clean_words.append(w)
            # If the last 3+ words repeat, stop
            if len(clean_words) >= 6:
                half = len(clean_words) // 2
                if clean_words[-half:] == clean_words[-2*half:-half]:
                    clean_words = clean_words[:-half]
                    break
        text = " ".join(clean_words)

        # Remove "## Step" artifacts from base model
        if "##" in text:
            text = text.split("##")[0].strip()

        return text
    except Exception as e:
        print(f"Generation error: {e}")
        return ""


