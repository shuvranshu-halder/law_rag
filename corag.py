def run_evaluation(
    questions, ground_truths, vectorstore,
    model, tokenizer, gen_greedy, gen_sample,
    strategy="greedy", max_length=6, num_samples=4,
    verbose=True
):
    """Run evaluation and also return retrieved contexts."""
    
    predictions = []
    chain_lengths = []
    all_contexts = []   # NEW

    for i, (q, gt) in enumerate(zip(questions, ground_truths)):

        if strategy == "baseline":
            docs = retrieve_docs(vectorstore, q)
            answer = baseline_rag(q, vectorstore, model, tokenizer, gen_greedy)
            chain_len = 0
            contexts = docs   # baseline context

        elif strategy == "greedy":
            chain = corag_greedy(
                q, vectorstore, model, tokenizer,
                gen_greedy, gen_sample, max_length
            )
            answer = chain.final_answer
            chain_len = len(chain.steps)

            # collect all retrieved docs from chain
            contexts = []
            for step in chain.steps:
                contexts.extend(step.retrieved_docs)

        elif strategy == "best_of_n":
            chain = corag_best_of_n(
                q, vectorstore, model, tokenizer,
                gen_greedy, gen_sample, num_samples, max_length
            )
            answer = chain.final_answer
            chain_len = len(chain.steps)

            contexts = []
            for step in chain.steps:
                contexts.extend(step.retrieved_docs)

        elif strategy == "tree_search":
            chain = corag_tree_search(
                q, vectorstore, model, tokenizer,
                gen_greedy, gen_sample, max_length
            )
            answer = chain.final_answer
            chain_len = len(chain.steps)

            contexts = []
            for step in chain.steps:
                contexts.extend(step.retrieved_docs)

        else:
            raise ValueError(f"Unknown strategy: {strategy}")

        predictions.append(answer)
        chain_lengths.append(chain_len)
        all_contexts.append(contexts)   #  store contexts

        if verbose:
            print(f"\n[{i+1}/{len(questions)}] Q: {q}")
            print(f"  GT: {gt}")
            print(f"  Pred: {answer}")

            if strategy != "baseline":
                print(f"  Chain steps: {chain_len}")
                for j, step in enumerate(chain.steps):
                    print(f"    Step {j+1}: Q='{step.sub_query}' -> A='{step.sub_answer}'")

    # ---- Metrics ----
    em = [compute_em(p, g) for p, g in zip(predictions, ground_truths)]
    f1 = [compute_f1(p, g) for p, g in zip(predictions, ground_truths)]
    sub = [compute_substring(p, g) for p, g in zip(predictions, ground_truths)]

    results = {
        "strategy": strategy,
        "chain_length": max_length,
        "num_samples": num_samples if strategy == "best_of_n" else None,
        "em": float(np.mean(em)),
        "f1": float(np.mean(f1)),
        "substring": float(np.mean(sub)),
        "avg_chain_len": float(np.mean(chain_lengths)),
        "num_questions": len(questions)
    }

    print(f"\n{'='*60}")
    print(f"Results: {strategy} (L={max_length})")
    print(f"{'='*60}")
    print(f"  Exact Match:    {results['em']:.4f}")
    print(f"  F1 Score:       {results['f1']:.4f}")
    print(f"  Substring:      {results['substring']:.4f}")
    print(f"  Avg Chain Len:  {results['avg_chain_len']:.1f}")
    print(f"{'='*60}\n")

    #  RETURN CONTEXTS ALSO
    return results, predictions, all_contexts


